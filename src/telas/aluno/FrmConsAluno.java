public void carregarModelAlunos(String nome) {
        MatriculaController matriculaController = new MatriculaController();
        String sql = "select m from ViewMatriculas m where m.nome like '%" + nome + "%' order by m.nome";
        listaAlunos = matriculaController.listar(sql);
        if (listaAlunos == null) {
            listaAlunos = new ArrayList<ViewMatriculas>();
        }
        modelAlunos = new ConsultaAlunosTableModel(listaAlunos);
        tabelaCursos.setModel(modelAlunos);
        tabelaCursos.getColumnModel().getColumn(0).setPreferredWidth(50);
        tabelaCursos.getColumnModel().getColumn(1).setPreferredWidth(200); 
        tabelaCursos.getColumnModel().getColumn(2).setPreferredWidth(200); 
        tabelaCursos.repaint();
    }
